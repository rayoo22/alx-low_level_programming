/**
 * op_add - adds
 * @a: first operand
 * @b: second operand
 * Return: result
*/
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtrats
 * @a: int
 * @b: int
 * Return: subtraction
*/
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiplies
 * @a: operand
 * @b: another one
 * Return: product
*/
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divides
 * @a: int
 * @b: int
 * Return: dividend
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - modulus
 * @a: opernad
 * @b: operand
 * Return: remainder of division
*/
int op_mod(int a, int b)
{
return (a % b);
}
