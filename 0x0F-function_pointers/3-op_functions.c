#include "3-calc.h"
/**
 * opp_add - an adding function
 * @a: first operand
 * @b: second operand
 * Returns: ressult of addition
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracting function
 * @a: first operand
 * @b: second operand
 * Returns: result of a subtracting
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplying function
 * @a: first operandd
 * @b: second operand
 * Return: result of multiplying
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - dividing function
 * @a: first operand
 * @b: second operand
 * Return: dividing result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus function
 * @a: first operand
 * @b: second operand
 * Return: modulus operand
 */
int op_mod(int a, int b)
{
	return (a % b);
}
